num1 = int(input("정수 입력 : "))
num2 = int(input("정수 입력 : "))

result = num1 + num2
print(num1, '+', num2, '=', result)
print('-' * 20)

result = num1 - num2
print(num1, '-', num2, '=', result)
print('-' * 20)

result = num1 * num2
print(num1, '*', num2, '=', result)
print('-' * 20)

result = num1 / num2
print(num1, '/', num2, '=', result)
print('-' * 20)

# 몫 연산자
result = num1 // num2
print(num1, '//', num2, '=', result)
print('-' * 20)

# 나머지 연산자
result = num1 % num2
print(num1, '%', num2, '=', result)
print('-' * 20)





